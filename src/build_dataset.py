import os
import numpy as np
import pandas as pd
import librosa

# =========================================================
# הגדרות כלליות של הפרויקט
# =========================================================

# תיקיית ההקלטות הגולמיות
RAW_DIR = "data/raw"

# קובץ CSV שניצור בסוף
OUTPUT_CSV = "data/processed/windows_index.csv"

# ---------------------------------------------------------
# קצב דגימה לעבודה
#
# ההקלטות בוצעו ב־44.1kHz,
# אבל לצורך עיבוד אפשר להמיר ל־22.05kHz
# כדי להקטין עומס חישובי בלי לאבד מידע משמעותי.
# ---------------------------------------------------------
SAMPLE_RATE = 22050

# ---------------------------------------------------------
# גודל חלון:
# כל חצי שנייה נוציא מאפיינים
# ---------------------------------------------------------
WINDOW_SEC = 0.5

# ---------------------------------------------------------
# Hop:
# כל כמה זמן החלון מתקדם
#
# 0.25 sec -> overlap של 50%
# נותן תחזית חלקה יותר בזמן
# ---------------------------------------------------------
HOP_SEC = 0.25


# =========================================================
# טעינת אודיו
# =========================================================
def load_audio(path, sr=SAMPLE_RATE):

    # -----------------------------------------------------
    # librosa:
    # טוען WAV
    # ממיר למונו
    # ומבצע resample ל־sr שבחרנו
    # -----------------------------------------------------
    y, sr = librosa.load(path, sr=sr, mono=True)

    # -----------------------------------------------------
    # נרמול עוצמה:
    # מביא את הסיגנל לטווח [-1, 1]
    #
    # חשוב כדי שעוצמת ההקלטה לא תשפיע
    # על המודל בצורה מלאכותית.
    # -----------------------------------------------------
    if np.max(np.abs(y)) > 0:
        y = y / np.max(np.abs(y))

    return y, sr


# =========================================================
# יצירת חלונות עבור קובץ בודד
# =========================================================
def create_windows_for_file(audio_path, cup_id, take_id):

    # טעינת האודיו
    y, sr = load_audio(audio_path)

    # -----------------------------------------------------
    # המרת זמן -> מספר דגימות
    # -----------------------------------------------------
    window_size = int(WINDOW_SEC * sr)
    hop_size = int(HOP_SEC * sr)

    # -----------------------------------------------------
    # אורך ההקלטה בשניות
    #
    # ישמש אותנו ליצירת label של fill %
    # -----------------------------------------------------
    duration_sec = len(y) / sr

    rows = []

    window_id = 0

    # -----------------------------------------------------
    # מעבר על האודיו בחלונות
    # -----------------------------------------------------
    for start in range(0, len(y) - window_size, hop_size):

        end = start + window_size

        # -------------------------------------------------
        # מרכז החלון בזמן
        #
        # נשתמש בו כדי להעריך
        # כמה הכוס מלאה באותו רגע
        # -------------------------------------------------
        center_sample = (start + end) / 2
        center_time = center_sample / sr

        # -------------------------------------------------
        # Ground Truth:
        #
        # הנחת עבודה:
        # המילוי ליניארי בזמן.
        #
        # כלומר:
        # חצי מזמן ההקלטה ≈ 50% מילוי
        #
        # זו הנחה הנדסית טובה מספיק
        # כאשר זרם הברז יחסית קבוע.
        # -------------------------------------------------
        fill_percent = 100 * center_time / duration_sec

        # הגנה שלא נעבור 100%
        fill_percent = min(fill_percent, 100)

        # -------------------------------------------------
        # שומרים מידע על החלון
        # -------------------------------------------------
        rows.append({

            # נתיב לקובץ
            "audio_path": audio_path,

            # זהות הכוס
            # חשוב ל־GroupKFold
            "cup_id": cup_id,

            # מספר הקלטה
            "take_id": take_id,

            # מספר חלון
            "window_id": window_id,

            # מיקום בדגימות
            "start_sample": start,
            "end_sample": end,

            # מיקום בזמן
            "start_time": start / sr,
            "end_time": end / sr,

            # label
            "fill_percent": fill_percent
        })

        window_id += 1

    return rows


# =========================================================
# בניית כל הדאטהסט
# =========================================================
def build_dataset():

    all_rows = []

    # -----------------------------------------------------
    # מעבר על כל הכוסות
    # -----------------------------------------------------
    for cup_name in sorted(os.listdir(RAW_DIR)):

        cup_path = os.path.join(RAW_DIR, cup_name)

        # דילוג אם זה לא תיקייה
        if not os.path.isdir(cup_path):
            continue

        cup_id = cup_name

        # -------------------------------------------------
        # מעבר על כל ההקלטות של הכוס
        # -------------------------------------------------
        for filename in sorted(os.listdir(cup_path)):

            # עובדים רק עם WAV
            if not filename.lower().endswith(".wav"):
                continue

            audio_path = os.path.join(cup_path, filename)

            # שם ההקלטה בלי .wav
            take_id = os.path.splitext(filename)[0]

            # יצירת חלונות
            rows = create_windows_for_file(
                audio_path,
                cup_id,
                take_id
            )

            all_rows.extend(rows)

            print(f"Processed: {audio_path}")
            print(f"Windows created: {len(rows)}")
            print()

    # -----------------------------------------------------
    # יצירת DataFrame
    # -----------------------------------------------------
    df = pd.DataFrame(all_rows)

    # -----------------------------------------------------
    # יצירת תיקייה אם לא קיימת
    # -----------------------------------------------------
    os.makedirs(os.path.dirname(OUTPUT_CSV), exist_ok=True)

    # -----------------------------------------------------
    # שמירת CSV
    #
    # זהו האינדקס הראשי של הפרויקט.
    # בהמשך נוסיף אליו features.
    # -----------------------------------------------------
    df.to_csv(OUTPUT_CSV, index=False)

    print("======================================")
    print(f"Saved dataset index to:")
    print(OUTPUT_CSV)
    print("======================================")

    print(f"Total windows: {len(df)}")
    print(f"Unique cups: {df['cup_id'].nunique()}")
    print(f"Unique recordings: {df['take_id'].nunique()}")


# =========================================================
# נקודת כניסה
# =========================================================
if __name__ == "__main__":

    build_dataset()